func TestIsAgentSupported(t *testing.T) {
	tests := []struct {
		name      string
		agentType string
		want      bool
	}{
		{
			name:      "claude slug is supported",
			agentType: "claude",
			want:      true,
		},
		{
			name:      "claude-code alias is supported",
			agentType: "claude-code",
			want:      true,
		},
		{
			name:      "codex is supported",
			agentType: "codex",
			want:      true,
		},
		{
			name:      "cursor is not supported",
			agentType: "cursor",
			want:      false,
		},
		{
			name:      "windsurf is not supported",
			agentType: "windsurf",
			want:      false,
		},
		{
			name:      "droid is not supported",
			agentType: "droid",
			want:      false,
		},
		{
			name:      "empty agent type is not supported",
			agentType: "",
			want:      false,
		},
		{
			name:      "unknown agent is not supported",
			agentType: "unknown-agent",
			want:      false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := isAgentSupported(tt.agentType)
			if got != tt.want {
				t.Errorf("isAgentSupported(%q) = %v, want %v", tt.agentType, got, tt.want)
			}
		})
	}
}

