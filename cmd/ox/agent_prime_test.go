func TestLoadTeamInstructions(t *testing.T) {
	tests := []struct {
		name      string
		files     map[string]string // filename -> content
		teamName  string
		wantNil   bool
		wantFiles []string
		wantIn    string // substring expected in content
	}{
		{
			name:    "empty dir returns nil",
			files:   map[string]string{},
			wantNil: true,
		},
		{
			name: "auto-generated AGENTS.md is skipped",
			files: map[string]string{
				"AGENTS.md": "# SageOx\n\n---\n*Generated by SageOx CLI v0.17.0 at 2026-01-01T00:00:00Z*\n",
			},
			wantNil: true,
		},
		{
			name: "real AGENTS.md is loaded",
			files: map[string]string{
				"AGENTS.md": "# Team Instructions\n\nUse Go for everything.\n",
			},
			teamName:  "TestTeam",
			wantFiles: []string{"AGENTS.md"},
			wantIn:    "Use Go for everything",
		},
		{
			name: "CLAUDE.md only is loaded",
			files: map[string]string{
				"CLAUDE.md": "# Claude Config\n\nPrefer simplicity.\n",
			},
			teamName:  "TestTeam",
			wantFiles: []string{"CLAUDE.md"},
			wantIn:    "Prefer simplicity",
		},
		{
			name: "both files are concatenated",
			files: map[string]string{
				"AGENTS.md": "# Agent Instructions\n\nRule one.\n",
				"CLAUDE.md": "# Claude Config\n\nRule two.\n",
			},
			teamName:  "TestTeam",
			wantFiles: []string{"AGENTS.md", "CLAUDE.md"},
			wantIn:    "Rule one",
		},
		{
			name: "boilerplate AGENTS.md plus real CLAUDE.md returns only CLAUDE.md",
			files: map[string]string{
				"AGENTS.md": "# SageOx\n\n---\n*Generated by SageOx CLI v0.17.0*\n",
				"CLAUDE.md": "# Claude Config\n\nReal instructions.\n",
			},
			teamName:  "TestTeam",
			wantFiles: []string{"CLAUDE.md"},
			wantIn:    "Real instructions",
		},
		{
			name:    "empty path returns nil",
			files:   nil,
			wantNil: true,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			// handle empty path case
			if tt.files == nil {
				got := loadTeamInstructions("", tt.teamName)
				if got != nil {
					t.Fatalf("loadTeamInstructions(\"\", ...) = non-nil, want nil")
				}
				return
			}

			dir := t.TempDir()
			for name, content := range tt.files {
				if err := os.WriteFile(filepath.Join(dir, name), []byte(content), 0644); err != nil {
					t.Fatalf("write %s: %v", name, err)
				}
			}

			got := loadTeamInstructions(dir, tt.teamName)

			if tt.wantNil {
				if got != nil {
					t.Fatalf("want nil, got %+v", got)
				}
				return
			}

			if got == nil {
				t.Fatal("want non-nil, got nil")
			}

			if got.TeamName != tt.teamName {
				t.Errorf("team_name = %q, want %q", got.TeamName, tt.teamName)
			}

			if len(got.Files) != len(tt.wantFiles) {
				t.Errorf("files = %v, want %v", got.Files, tt.wantFiles)
			} else {
				for i, f := range tt.wantFiles {
					if got.Files[i] != f {
						t.Errorf("files[%d] = %q, want %q", i, got.Files[i], f)
					}
				}
			}

			if tt.wantIn != "" && !strings.Contains(got.Content, tt.wantIn) {
				t.Errorf("content missing %q, got: %s", tt.wantIn, got.Content)
			}

			if got.Size != len(got.Content) {
				t.Errorf("size = %d, want %d (len of content)", got.Size, len(got.Content))
			}

			if got.Tokens == 0 {
				t.Error("tokens = 0, want > 0")
			}
		})
	}
}

